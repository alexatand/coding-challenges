rows = {'1' => '', '2' => '', '3' => ''}

number_pattern = {
  '1' => ['   ', '  |', '  |'],
  '2' => [' _ ', ' _|', '|_ '],
  '3' => [' _ ', ' _|', ' _|'],
  '4' => ['   ', '|_|', '  |'],
  '5' => [' _ ', '|_ ', ' _|'],
  '6' => [' _ ', '|_ ', '|_|'],
  '7' => [' _ ', '  |', '  |'],
  '8' => [' _ ', '|_|', '|_|'],
  '9' => [' _ ', '|_|', ' _|'],
  '0' => [' _ ', '| |', '|_|'],
}

ARGV[0].chars.each do |a|
  rows['1'] += number_pattern[a][0]
  rows['2'] += number_pattern[a][1]
  rows['3'] += number_pattern[a][2]
end

p rows['1']
p rows['2']
p rows['3']
